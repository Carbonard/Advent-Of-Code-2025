int	ft_isdigit(int c)
{
	if (!('0' <= c && c <= '9'))
		return (0);
	else
		return (2048);
}

int	ft_ishex(int c)
{
	if (!(('0' <= c && c <= '9') || ('a' <= c && c <= 'f')
			|| ('A' <= c && c <= 'F')))
		return (0);
	else
		return (2048);
}
